### 9. Your Rights

Under UK GDPR, you have the following rights:

#### 9.1 Right to Be Informed
You have the right to know how we use your data (this policy explains that).

#### 9.2 Right of Access
You can request a copy of the personal data we hold about you.

**How to request:**
- Email [EMAIL] or write to [ADDRESS]
- We'll respond within 30 days
- Free of charge
- We may need to verify your identity

#### 9.3 Right to Rectification
If your data is inaccurate or incomplete, you can ask us to correct it.

**How to request:**
- Contact us with corrections
- We'll update within 30 days
- Free of charge

#### 9.4 Right to Erasure ("Right to Be Forgotten")
You can ask us to delete your data in certain circumstances.

**When we can delete:**
- Data no longer needed for original purpose
- You withdraw consent (for marketing)
- Data processed unlawfully

**When we CANNOT delete:**
- Legal obligation to keep it (tax, gas safety records)
- Needed for legal claims
- Required for contract fulfillment

We'll explain if we cannot delete and why.

#### 9.5 Right to Restrict Processing
You can ask us to limit how we use your data (e.g., while investigating a complaint).

#### 9.6 Right to Data Portability
You can request your data in a commonly used format (not usually relevant for trades services).

#### 9.7 Right to Object
You can object to:
- Marketing (we'll stop immediately)
- Processing based on legitimate interests (we'll stop unless compelling reason)

#### 9.8 Rights Related to Automated Decision Making
We do not use automated decision-making or profiling.

**To exercise any rights:**
Contact: [NAME] at [EMAIL] or [PHONE]  
We will respond within 30 days.

